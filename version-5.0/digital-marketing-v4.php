   <script>
    document.addEventListener("DOMContentLoaded", function () {
      const autoSlideSection = document.querySelector("#tabs-section-4.auto-slide-tabs");
      if (!autoSlideSection) return;

      const tabs = autoSlideSection.querySelectorAll(".tab");
      const contents = autoSlideSection.querySelectorAll(".content");
      let currentIndex = 0;
      let intervalId;

      function activateTab(index) {
        tabs.forEach((tab, i) => {
          tab.classList.toggle("active", i === index);
          contents[i].classList.toggle("active", i === index);
        });
      }

      function nextTab() {
        currentIndex = (currentIndex + 1) % tabs.length;
        activateTab(currentIndex);
      }

      // Also update currentIndex if user clicks a tab
      tabs.forEach((tab, i) => {
        tab.addEventListener("click", function () {
          currentIndex = i;
        });
      });

      // Start auto-slide
      intervalId = setInterval(nextTab, 2000);

      // Optional: Pause on hover
      autoSlideSection.addEventListener("mouseenter", () => clearInterval(intervalId));
      autoSlideSection.addEventListener("mouseleave", () => {
        intervalId = setInterval(nextTab, 2000);
      });
    });
