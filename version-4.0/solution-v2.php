          const rect = item.getBoundingClientRect();
          if (rect.top + rect.height / 2 - panelTop < panelH * 0.65) {
            activeIndex = i;
          }
        });

        if (activeIndex >= 0) {
          items.forEach((item, i) => item.classList.toggle('active', i <= activeIndex));
          progressEl.style.height = pctForStep(activeIndex) + '%';
        }
      }, {
        passive: true
      });

    })();











